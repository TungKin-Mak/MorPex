function getArtifactContent(taskId: string, round: number): string {
  const template = CONTENT_MAP.get(taskId);
  if (!template) return '# Generated Artifact\nRound ' + round + '\n';
  return template.replace(/\$\{round\}/g, String(round))
    .replace(/\$\{R\}/g, String(round));
}

// ── Pipeline Execution Simulation ──

async function executePipeline(task: TaskDef, round: number, opts?: { injectDeviation?: boolean; simulateStress?: boolean }) {
  const pipelineId = `pl_${task.id}_r${round}_${Date.now()}`;
  const start = Date.now();
  const stages: any[] = [];
  let deviationCount = 0;
  let reflexTriggered = false;
  let patchApplied = false;
  let winnerOverride: string | null = null;
  let riskMode = 'efficiency';

  // S1: Intent Analysis
  stages.push({ stage: 1, name: 'Intent Analysis', status: 'completed', detail: `domain=${task.domain}, confidence=0.85` });

  // S2: Experience Retrieval  
  stages.push({ stage: 2, name: 'Experience Retrieval', status: 'completed', detail: 'pos=0 neg=0 vec=0' });

  // S3: Candidate Generation
  stages.push({ stage: 3, name: 'Candidate Generation', status: 'completed', detail: 'aggressive(3n) defensive(7n) fallback(2n) fallbackTemplate=true' });

  // S4: DES Simulation
  const aggrS = opts?.simulateStress ? 0.65 : 1.0;
  const defS = opts?.simulateStress ? 0.90 : 1.0;
  stages.push({ stage: 4, name: 'DES Simulation', status: 'completed', detail: `aggr=${(aggrS*100).toFixed(0)}% def=${(defS*100).toFixed(0)}% fb=100%` });

  // S5: MCDA Evaluation
  let aScore = 0.54, dScore = 0.52, fScore = 0.44;
  if (opts?.simulateStress) {
    deviationCount = 1; riskMode = 'stability';
    aScore = 0.32; dScore = 0.58; fScore = 0.49;
    winnerOverride = 'defensive';
  }
  const winner = winnerOverride || (aScore >= dScore && aScore >= fScore ? 'aggressive' : dScore >= fScore ? 'defensive' : 'fallback');
  stages.push({ stage: 5, name: 'MCDA Evaluation', status: 'completed', detail: `winner=${winner} weights: stability=${opts?.simulateStress?0.30:0.10}` });

  // S6: Decision Trace
  stages.push({ stage: 6, name: 'Decision Trace', status: 'completed', detail: 'writtenToDisk=true' });

  // S7: Activation
  stages.push({ stage: 7, name: 'Activation', status: 'completed', detail: `activated=${winnerOverride||'aggressive'} ready=true` });

  if (opts?.injectDeviation) {
    deviationCount = 1; reflexTriggered = true; patchApplied = Math.random() > 0.2;
  }

  const total = Date.now() - start;
  return {
    pipelineId, stages, winner: winnerOverride || 'aggressive', totalDurationMs: total, success: true,
    deviationCount, reflexTriggered, patchApplied, winnerOverride, riskMode,
    trace: { pipelineId, taskId: task.id, round, winner: winnerOverride || 'aggressive', deviationCount, reflexTriggered, patchApplied, winnerOverride, riskMode },
  };
}

// ── Run a Round ──

async function runRound(round: number, name: string, opts?: { injectDeviation?: boolean; simulateStress?: boolean }): Promise<any> {
  console.log(`\n${BRIGHT}${'='.repeat(70)}${RESET}`);
  console.log(`${BRIGHT} Round ${round}: ${name} ${RESET}`);
  console.log(`${BRIGHT} 10 concurrent tasks | injection=${!!opts?.injectDeviation} stress=${!!opts?.simulateStress}${RESET}`);
  console.log(`${BRIGHT}${'='.repeat(70)}${RESET}`);

  const results = await Promise.all(TASKS.map(async (task) => {
    const tStart = Date.now();
    let passed = true, error: string | undefined;
    let pipelineResult: any = null;

    try {
      // Execute pipeline
      pipelineResult = await executePipeline(task, round, opts);
      // Write artifact
      const dir = path.join(WORKSPACE, `round${round}`, task.id);
      const filePath = path.join(dir, task.artifact);
      const content = getArtifactContent(task.id, round);
      await fsp.mkdir(dir, { recursive: true });
      await fsp.writeFile(filePath, content, 'utf-8');
    } catch (err: any) {
      passed = false; error = err.message;
    }

    const dur = Date.now() - tStart;
    return { task, round, passed, durationMs: dur, error, pipelineResult };
  }));

  // Print results
  let passed = 0, failed = 0, totalDev = 0, totalPatch = 0, totalOverride = 0, totalTime = 0;
  for (const r of results) {
    const icon = r.passed ? PASS : FAIL;
    const typeIcon = r.task.type === 'programming' ? '\u{1F4BB}' : r.task.type === 'planning' ? '\u{1F4CB}' : '\u{1F4CA}';
    const dur = (r.durationMs / 1000).toFixed(1) + 's';
    let extra = '';
    if (r.pipelineResult?.deviationCount) extra += ` [deviation:${r.pipelineResult.deviationCount}`;
    if (r.pipelineResult?.reflexTriggered) extra += ' reflex:triggered';
    if (r.pipelineResult?.patchApplied) extra += ' patch:applied';
    if (r.pipelineResult?.winnerOverride) extra += ` override:${r.pipelineResult.winnerOverride}`;
    if (r.pipelineResult?.riskMode === 'stability') extra += ' risk:stability';
    if (extra) extra += ']';
    console.log(`  ${typeIcon} ${icon} ${CYAN}${r.task.id.padEnd(25)}${RESET} ${dur.padEnd(8)} ${extra || ''}`);
    if (r.passed) passed++; else failed++;
    totalDev += r.pipelineResult?.deviationCount ?? 0;
    if (r.pipelineResult?.patchApplied) totalPatch++;
    if (r.pipelineResult?.winnerOverride) totalOverride++;
    totalTime += r.durationMs;
  }

  const avg = passed + failed > 0 ? Math.round(totalTime / (passed + failed)) : 0;
  console.log(`\n${BRIGHT}Round ${round}: ${passed}/${passed+failed} passed | avg ${(avg/1000).toFixed(1)}s | deviations:${totalDev} patches:${totalPatch} overrides:${totalOverride}${RESET}`);
  return { round, name, passed, failed, avgDurationMs: avg, totalDeviations: totalDev, totalPatches: totalPatch, overrideCount: totalOverride, results };
}

